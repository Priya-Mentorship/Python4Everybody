import numpy as np
arr = np.array([10,20,30,40,50,60])

print(np.min(arr)) # min value
print(np.max(arr)) # max value
print(np.argmax(arr)) # position of max value
print(np.argmin(arr)) # position of min value
print(np.sin(arr)) # sin value
print(np.cos(arr)) # cos value
print(np.linspace(1,3,10)) # number of values between range equally

